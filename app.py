# --- Helpers ---
def hash_password(password: str) -> str:
    return hashlib.sha256(password.encode()).hexdigest()


def get_current_user() -> dict | None:
    if 'user_id' in session:
        if DB_CONNECTED and users_collection is not None:
            try:
                user = users_collection.find_one({"_id": ObjectId(session['user_id'])})
                return user
            except Exception:
                return None
        elif session['user_id'] == 'demo_user':
            return {
                "_id": "demo_user",
                "name": session.get('user_name', "Demo User"),
                "email": "demo@example.com",
                "plan": session.get('demo_plan', 'free'),
                "subscription": {}
            }
    return None


def get_user_plan(user: dict | None) -> str:
    """Return the user's current plan slug, checking for expiry."""
    if user is not None:
        # Check for subscription object
        sub = user.get("subscription", {})
        plan = user.get("plan", "free")
        
        if plan != "free" and sub.get("status") == "active":
            expiry_str = sub.get("expiry_date")
            if expiry_str:
                expiry_dt = datetime.fromisoformat(expiry_str.replace('Z', ''))
                if datetime.utcnow() > expiry_dt:
                    # Plan has expired! Revert in DB
                    if DB_CONNECTED and users_collection is not None:
                        users_collection.update_one(
                            {"_id": user["_id"]},
                            {"$set": {"plan": "free", "subscription.status": "expired"}}
                        )
                    return "free"
            return plan
        return plan
        
    # Check session for demo plan if no DB user
    return session.get("demo_plan", "free")


def get_proposals_this_month(user_id: str) -> int:
    """Count proposals generated by this user in the current calendar month."""
    if not DB_CONNECTED or proposals_collection is None:
        return int(session.get('demo_proposal_count', 0))
    assert proposals_collection is not None  # type narrowing for Pyre2
    now = datetime.utcnow()
    month_start = datetime(now.year, now.month, 1).isoformat() + "Z"
    count: int = proposals_collection.count_documents({
        "user_id": user_id,
        "created_at": {"$gte": month_start}
    })
    return count


