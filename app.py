# --- AI Generation ---
def generate_proposal_with_ai(client_name: str, project_goal: str, timeline: int, price: Optional[float] = None, refinement_note: Optional[str] = None) -> dict:
    """
    Call OpenRouter AI to generate a detailed proposal.
    If price is None, AI will predict it.
    """
    if not OPENROUTER_API_KEY or "***" in OPENROUTER_API_KEY:
        print("⚠️ OPENROUTER_API_KEY is missing or masked. Please provide a full key.")
        return generate_proposal_content(client_name, project_goal, price or 5000, timeline)

    # Using openrouter/free for high availability and zero cost
    model = "openrouter/free" 
    print(f"🚀 Generating proposal using model: {model}")
    
    price_prompt = f"Predicted price: ${price}" if price else "Predict a fair market price for this project."
    refine_prompt = f"Refinement request: {refinement_note}" if refinement_note else ""

    prompt = f"""
    You are a world-class business strategist and proposal writer for a premium agency. 
    Your goal is to transform basic project ideas into persuasive, high-value professional proposals.

    ### Project Context:
    - **Client:** {client_name}
    - **Project Objective:** {project_goal}
    - **Proposed Timeline:** {timeline} weeks
    - **Budget Target:** {price_prompt}
    - **Special Refinement Instructions:** {refinement_note if refinement_note else "None. Create a fresh, comprehensive proposal."}

    ### Instructions for Analysis:
    1. **Executive Summary:** Analyze the project objective and write a compelling summary. Focus on the 'Why' - the business impact, the transformation, and the value proposition. Use professional, modern business language.
    2. **Scope of Work:** Detail exactly WHAT will be done. Break it down into technical requirements, design phases, and business goals. Use bullet points for clarity (in the string).
    3. **Timeline & Milestones:** Create {math.ceil(timeline/2) if timeline > 0 else 2} distinct logical phases. Each phase must have a title, a duration in weeks, a list of at least 3-4 specific deliverables, and a logical price allocation.
    4. **Pricing:** Ensure the total of all milestone prices EQUALS the final total project price. Output prices as numbers (floats).
    5. **Tone:** Premium, authoritative, yet approachable.

    ### output_format (MANDATORY JSON):
    {{
        "id": "PROP-unique-id",
        "createdAt": "ISO-TIMESTAMP",
        "client": {{ 
            "name": "{client_name}", 
            "company": "If the client name implies a company like 'Sarah at Google', put 'Google' here, otherwise use a professional placeholder like '{client_name} Solutions'"
        }},
        "project": {{ 
            "title": "A compelling, high-value project title based on the goal", 
            "goal": "{project_goal}", 
            "timeline": {timeline}, 
            "price": predicted_or_provided_price 
        }},
        "sections": {{
            "executiveSummary": "3-4 paragraphs of deep analysis and strategy...",
            "scopeOfWork": "A detailed multi-paragraph breakdown of activities and standards...",
            "milestones": [
                {{ 
                    "id": 1, 
                    "title": "Discovery & Strategy Architecure", 
                    "duration": "1 week", 
                    "deliverables": ["Technical Audit", "UI/UX Sitemap", "Project Roadmap"], 
                    "price": 1500.00 
                }}
            ],
            "investment": {{ 
                "total": total_price, 
                "breakdown": "Summarize the payment terms (e.g., 25% upfront, etc.)" 
            }},
            "nextSteps": "Professional closing statement and instructions for signing."
        }}
    }}

    Return ONLY the raw JSON object. No preamble or post-script.
    """

    try:
        response = requests.post(
            url="https://openrouter.ai/api/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {OPENROUTER_API_KEY}",
                "HTTP-Referer": "http://localhost:5000", # Optional
                "X-Title": "ProposeAI", # Optional
            },
            data=json.dumps({
                "model": model,
                "messages": [{"role": "user", "content": prompt}],
                "response_format": { "type": "json_object" }
            }),
            timeout=30
        )
        
        if response.status_code != 200:
            print(f"❌ OpenRouter API Error: {response.status_code} - {response.text}")
            raise Exception(f"AI Service Error: {response.status_code}")

        res_json = response.json()
        if 'choices' not in res_json:
            print(f"❌ Unexpected API Response: {res_json}")
            raise Exception("AI did not return a valid completion.")

        raw_content = res_json['choices'][0]['message']['content']
        print(f"✅ AI Response Received: {raw_content[:100]}...")
        proposal = json.loads(raw_content)
        
        # Ensure ID and timestamps are updated
        proposal["id"] = f"PROP-{int(time.time() * 1000)}"
        proposal["createdAt"] = datetime.utcnow().isoformat() + "Z"
        
        return proposal
    except Exception as e:
        print(f"❌ AI Generation Error: {e}")
        # Return fallback with clear notice in the logs
        return generate_proposal_content(client_name, project_goal, price or 5000, timeline)


