@app.route('/api/generate-proposal', methods=['POST'])
def generate_proposal():
    """
    Generate a proposal from 4 inputs — enforces plan limits.
    """
    try:
        data = request.get_json()
        if not data:
            return jsonify({"success": False, "error": "No data received."}), 400

        client_name: str = str(data.get('clientName') or 'Unknown Client').strip()
        project_goal: str = str(data.get('projectGoal') or 'To be defined').strip()
        price_raw = data.get('projectPrice')
        price: Optional[float] = float(price_raw) if price_raw and str(price_raw).strip() else None
        timeline: int = int(data.get('timeline') or 4)

        if not client_name or not project_goal:
            return jsonify({"success": False, "error": "Client name and project goal are required."}), 400

        # --- PLAN ENFORCEMENT ---
        user = get_current_user()
        plan: str = get_user_plan(user)
        limits: dict = PLAN_LIMITS.get(plan, PLAN_LIMITS["free"])
        user_id: str = session.get('user_id', 'anonymous')

        monthly_limit = limits["proposals_per_month"]
        if monthly_limit is not None:
            used: int = get_proposals_this_month(user_id)
            if used >= monthly_limit:
                msg: str = PLAN_UPGRADE_MSG["proposals_per_month"].format(limit=monthly_limit, plan=plan.capitalize())
                return jsonify({
                    "success": False,
                    "error": msg,
                    "upgrade_required": True,
                    "feature": "proposals_per_month"
                }), 403

        # Generate proposal
        proposal: dict = generate_proposal_with_ai(client_name, project_goal, timeline, price)

        # Save to MongoDB if connected
        if DB_CONNECTED and proposals_collection is not None:
            try:
                doc = {
                    "user_id": user_id,
                    "proposal_id": proposal["id"],
                    "client_name": client_name,
                    "raw_input": {
                        "project_goal": project_goal,
                        "project_price": price,
                        "timeline": timeline
                    },
                    "generated_output": proposal,
                    "status": "Draft",
                    "plan_at_creation": plan,
                    "created_at": datetime.utcnow().isoformat() + "Z"
                }
                proposals_collection.insert_one(doc)
            except Exception as db_err:
                print(f"DB save error (non-fatal): {db_err}")
        else:
            # Demo mode: increment session counter
            session['demo_proposal_count'] = int(session.get('demo_proposal_count', 0)) + 1

        return jsonify({
            "success": True,
            "proposal": proposal,
            "plan": plan,
            "used_this_month": get_proposals_this_month(user_id)
        }), 200

    except Exception as e:
        return jsonify({"success": False, "error": str(e)}), 500


@app.route('/api/refine-proposal', methods=['POST'])
def refine_proposal():
    """Refine an existing proposal (increase/decrease budget)."""
    try:
        data = request.get_json()
        current_proposal = data.get('proposal')
        action = data.get('action') # 'increase', 'decrease'
        
        if not current_proposal or not action:
            return jsonify({"success": False, "error": "Missing data."}), 400
            
        client_name = current_proposal['client']['name']
        project_goal = current_proposal['project']['goal']
        timeline = current_proposal['project']['timeline']
        current_price = current_proposal['project']['price']
        
        # Adjust price by 20%
        if action == 'increase':
            new_price = current_price * 1.2
            refinement_note = "Increase the scope and add more advanced features to justify a 20% budget increase."
        else:
            new_price = current_price * 0.8
            refinement_note = "Reduce the scope to fit a 20% budget decrease while keeping core features."
            
        new_proposal = generate_proposal_with_ai(client_name, project_goal, timeline, new_price, refinement_note)
        
        return jsonify({"success": True, "proposal": new_proposal}), 200
        
    except Exception as e:
        return jsonify({"success": False, "error": str(e)}), 500
        

