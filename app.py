@app.route('/api/proposal/<proposal_id>/rename', methods=['POST'])
def rename_proposal(proposal_id):
    """Rename a specific proposal."""
    try:
        user_id = session.get('user_id', 'anonymous')
        data = request.get_json()
        
        if not data or 'title' not in data:
            print(f"DEBUG ERROR: Missing data in rename request for {proposal_id}")
            return jsonify({"success": False, "error": "New title is required (missing JSON data)."}), 400
            
        new_title = data.get('title', '').strip()
        
        if not new_title:
            return jsonify({"success": False, "error": "New title cannot be empty."}), 400

        if DB_CONNECTED and proposals_collection is not None:
            # We update the title inside the generated_output object
            print(f"DEBUG: Renaming proposal {proposal_id} for user {user_id} to '{new_title}'")
            result = proposals_collection.update_one(
                {"proposal_id": proposal_id, "user_id": user_id},
                {"$set": {"generated_output.project.title": new_title}}
            )
            print(f"DEBUG: Update result - Matched: {result.matched_count}, Modified: {result.modified_count}")
            
            if result.matched_count == 0:
                # Let's check if the proposal exists at all with ANY user_id for debugging
                exists = proposals_collection.find_one({"proposal_id": proposal_id})
                if exists:
                    print(f"DEBUG WARNING: Proposal {proposal_id} exists but is owned by {exists.get('user_id')}, not {user_id}")
                else:
                    print(f"DEBUG WARNING: Proposal {proposal_id} not found at all in DB.")
                return jsonify({"success": False, "error": "Proposal not found or access denied."}), 404
                
            return jsonify({"success": True, "message": "Proposal renamed successfully!"})
        else:
            return jsonify({"success": False, "error": "Database not connected."}), 503

    except Exception as e:
        print(f"DEBUG CRITICAL ERROR in rename: {str(e)}")
        return jsonify({"success": False, "error": str(e)}), 500


